class Gallery extends Component {
  state = {imageId: imagesList[0].id}

  updateImage = id => {
    const imageItem = imagesList.filter(each => each.id === id)

    this.setState({imageId: imageItem[0].id})
  }

  render() {
    const {imageId} = this.state
    const GalleryPic = imagesList.filter(each => each.id === imageId)
    const {imageUrl, imageAltText} = GalleryPic[0]

    return (
      <div className="container">
        <div className="bgContainer">
          <div className="GalleryContainer">
            <img src={imageUrl} alt={imageAltText} className="image" />
          </div>
          <div className="headContainer">
            <h1 className="head">Nature Photography</h1>
            <p className="para">Nature Photography by Rahul</p>
          </div>
          <ul className="thumbnailContainer">
            {imagesList.map(each => (
              <ThumbnailItem
                items={each}
                key={each.id}
                updateImage={this.updateImage}
                isActive={imageId === each.id}
              />
            ))}
          </ul>
        </div>
      </div>
    )
  }
}
export default Gallery
